package cs3500.pa05.model;

/**
 * An Enumeration of the days of the week
 */
public enum DayOfWeek {
  /**
   * Represents the weekday Sunday
   */
  SUNDAY,
  /**
   * Represents the weekday Monday
   */
  MONDAY,
  /**
   * Represents the weekday Tuesday
   */
  TUESDAY,
  /**
   * Represents the weekday Wednesday
   */
  WEDNESDAY,
  /**
   * Represents the weekday Thursday
   */
  THURSDAY,
  /**
   * Represents the weekday Friday
   */
  FRIDAY,
  /**
   * Represents the weekday Saturday
   */
  SATURDAY;
}
